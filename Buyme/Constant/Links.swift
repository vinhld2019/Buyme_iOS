var hasOld: Bool = false
var hasNew: Bool = false
var hasStore: Bool = false

var getLinks: [String] {
    var links: [String] = []
    
    if hasNew {
        links.append(contentsOf: newLinks)
    }
    
    if hasOld {
        links.append(contentsOf: oldLinks)
    }
    
    if hasStore {
        links.append(contentsOf: storeLinks)
    }
    
    return links
}

var linkIsValid: Bool { hasNew || hasOld || hasStore }

var playerActionIsHidden: Bool = true

func randomLinks(_ numberOfLink: Int) -> [String] {
    var links: [String] = []
    var indexs: [Int] = []
    let all = getLinks
    while links.count < numberOfLink {
        let n = Int.random(in: 0...all.count-1)
        if indexs.contains(n) {
           continue
        } else {
            indexs.append(n)
            links.append(all[n])
        }
    }
    return links
}

